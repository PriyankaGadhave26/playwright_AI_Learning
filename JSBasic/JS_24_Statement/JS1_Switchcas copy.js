//swich case
//sun=0;mon=1;tue=2;wed=3;thu=4;fri=5;sat=6
let day =3;
switch(day)
{    case 0:
        console.log('sunday');
        break;
    case 1:
        console.log('monday');
        break;                                      
    case 2:
        console.log('tuesday');
        break;  
    case 3:
        console.log('wednesday');
        break;  
    case 4:
        console.log('thursday');
        break;  
    case 5:
        console.log('friday');
        break;
    case 6:
        console.log('saturday');
        break;  
    default:
        console.log('invalid day');
}

